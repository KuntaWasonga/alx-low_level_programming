
#include "3-calc.h"

/**
 * op_add - function that adds two numbers
 * @a: first number
 * @b: second number
 *
 * Return: returns sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - function that subtracts b from a
 * @a: first number
 * @b: second number
 *
 * Return: returns difference of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - function that multiplies two numbers
 * @a: first number
 * @b: second number
 *
 * Return: returns product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - function that divides a by b
 * @a: first number
 * @b: second number
 *
 * Return: returns result of the division of a by b
 */
int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - function that gets the remainder of the division of two numbers
 * @a: first number
 * @b: second number
 *
 * Return: returns remainder of the division of a by b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
